public class Array_int {
    public static void main(String[] args) {

        // Declaração de um array de inteiros
        int[] num = {10, 20, 30, 40, 50};

        System.out.println(num[0]); // Acessa o primeiro elemento
        System.out.println(num[1]); // Acessa o segundo elemento
        System.out.println(num[2]); // Acessa o terceiro elemento

        num[1] = 25; // Modifica o segundo elemento

        System.out.println(num[1]); // Mostra o segundo elemento modificado
    }
}