// Classe que contém os métodos da calculadora
class metodos {
    // Método de soma
    public int somar(int a, int b) {
        return a + b;
    }

    // Método de subtração
    public int subtrair(int a, int b) {
        return a - b;
    }

    // Método de multiplicação
    public int multiplicar(int a, int b) {
        return a * b;
    }

    // Método de divisão
    public int dividir(int a, int b) {
        return a / b;
    }
}
